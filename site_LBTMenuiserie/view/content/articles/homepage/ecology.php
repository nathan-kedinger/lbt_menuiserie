    <h2>Une dimension écologique</h2>

    <p>Le choix des matériaux se fait avec un regard appuyé sur leur provenance et sur le respect 
        des différentes normes. Nous proposons en particulier du bois massif poussé localement 
        dans un souci écologique. L'écologie est au cœur des valeurs de notre entreprise.</p><br>
    
    
    <p>La fabrication est pensée pour réutiliser au maximum les chutes de bois en de plus 
        petits objets. Les copeaux de bois sont conservés et partagés avec des gens possédants 
        des toilettes sèches ou un potager...(n'hésitez pas à nous contacter) Nous 
        essayons de valoriser au maximum nos déchets, d'en produire le minimmum et de les rendre 
        le moins nocif possible pour notre planète.</p>

