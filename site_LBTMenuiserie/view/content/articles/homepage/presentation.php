    <h2>Artisan Menuisier Fabricant et Poseur</h2><br>
    <p>Bastien LEGENDRE est le gérant de l'entreprise LBT MENUISERIE. Celle-ci se situe dans le Triève au coeur des montagnes, à Monestier-de-Clèrmont 
        près de Grenoble, en Isère.  
        Monsieur LEGENDRE s'est formé en menuiserie auprès des Compagnons du Devoir. Il y a appri le métier de menuisier, agenceur
        et créateur de mobilier. Enrichi par de nombreuses expériences dans le bois, il décide en 2021 de
        créer une entreprise à son image : rigoureuse et organisée.</p><br>
        <p>Aujourd'hui, l'entreprise spécialisée dans le travail du bois fabrique et pose sur grenoble et dans toute sa région. 
            Le siège étant situé à Monestier-de-Clèrmont, l'entreprise qui s'implante dans une démarche locale, 
            oeuvre principalement dans les villes les plus proches :
        </p>
        <ul>
            <li>Roissard</li>
            <li>Avignonet</li>
            <li>Montenard</li>
            <li>Vif</li>
            <li>Villard-de-Lans</li>
            <li>Lavars</li>
            <li>Echirolles</li>
            <li>Chamrousse</li>
        </ul>
